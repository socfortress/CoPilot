<template>
	<n-popover v-model:show="show" trigger="manual" to="body" content-class="px-0" @clickoutside="closePopup()">
		<template #trigger>
			<slot :loading :toggle-popup />
		</template>

		<div class="flex min-w-52 flex-col justify-center gap-2 py-1">
			<div class="flex items-center gap-2">
				<n-button :loading type="success" @click="updateActive(true)">Activates all Sigma queries</n-button>
				<n-button :loading @click="updateActive(false)">Deactivates all Sigma queries</n-button>
			</div>

			<p class="text-right">* It may take several minutes</p>

			<div class="flex justify-between gap-2">
				<n-button quaternary size="small" @click="closePopup()">Close</n-button>
			</div>
		</div>
	</n-popover>
</template>

<script setup lang="ts">
import Api from "@/api"
import { NButton, NPopover, useMessage } from "naive-ui"
import { ref } from "vue"

const emit = defineEmits<{
	(e: "updated"): void
}>()

const loading = defineModel<boolean | undefined>("loading", { default: false })

const show = ref(false)
const lastShow = ref(new Date().getTime())
const message = useMessage()

function togglePopup() {
	if (new Date().getTime() - lastShow.value > 500) {
		show.value = !show.value
	}
}

function closePopup() {
	lastShow.value = new Date().getTime()
	show.value = false
}

function updateActive(active: boolean) {
	loading.value = true

	const methods = active ? "activateAllQueries" : "deactivateAllQueries"

	Api.sigma[methods]()
		.then(res => {
			if (res.data.success) {
				emit("updated")
				message.success(res.data?.message || "Sigma queries updated successfully")
			} else {
				message.warning(res.data?.message || "An error occurred. Please try again later.")
			}
		})
		.catch(err => {
			message.error(err.response?.data?.message || "An error occurred. Please try again later.")
		})
		.finally(() => {
			loading.value = false
		})
}
</script>
